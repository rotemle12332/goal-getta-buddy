const dictionaries: Record<Lang, Dict> = { en, es, fr, de, pt, it, ja, zh, ar, he };

function format(s: string, vars?: Record<string, string | number>) {
  if (!vars) return s;
  return s.replace(/\{(\w+)\}/g, (_, k) => String(vars[k] ?? ""));
}

type Ctx = {
  lang: Lang;
  setLang: (l: Lang) => void;
  t: (key: string, vars?: Record<string, string | number>) => string;
  dir: "ltr" | "rtl";
  locale: string;
};

const LangContext = createContext<Ctx | null>(null);

function detectLang(): Lang {
  if (typeof window === "undefined") return "en";
  const saved = localStorage.getItem("goaly_lang") as Lang | null;
  if (saved && dictionaries[saved]) return saved;
  const nav = (navigator.language || "en").slice(0, 2) as Lang;
  return dictionaries[nav] ? nav : "en";
}

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>("en");

  useEffect(() => {
    setLangState(detectLang());
  }, []);

  const meta = LANGUAGES.find((l) => l.code === lang) ?? LANGUAGES[0];

  useEffect(() => {
    if (typeof document === "undefined") return;
    document.documentElement.lang = lang;
    document.documentElement.dir = meta.dir;
  }, [lang, meta.dir]);

  const setLang = (l: Lang) => {
    setLangState(l);
    try {
      localStorage.setItem("goaly_lang", l);
    } catch {
      /* ignore */
    }
  };

  const value = useMemo<Ctx>(
    () => ({
      lang,
      setLang,
      dir: meta.dir,
      locale: meta.locale,
      t: (key, vars) => {
        const v = dictionaries[lang]?.[key] ?? dictionaries.en[key] ?? key;
        return format(v, vars);
      },
    }),
    [lang, meta.dir, meta.locale],
  );

  return <LangContext.Provider value={value}>{children}</LangContext.Provider>;
}

export function useT() {
  const ctx = useContext(LangContext);
  if (!ctx) {
    // Safe fallback so any tree without provider still works.
    return {
      t: (key: string, vars?: Record<string, string | number>) => format(en[key] ?? key, vars),
      lang: "en" as Lang,
      setLang: () => {},
      dir: "ltr" as const,
      locale: "en-US",
    };
  }
  return ctx;
}